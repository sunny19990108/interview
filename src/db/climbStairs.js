/**
 * 爬楼梯
 * 假设你正在爬楼梯。需要 n 阶你才能到达楼顶
 * 每次你可以爬 1 或 2 个台阶。你有多少种不同的方法可以爬到楼顶呢
 */

/**
 * 思路：
 * db[0]: 0
 * db[1]: 1
 * db[2]: 2
 * db[n]: db[n-1] + db[n-2] 到达第n个台阶有从第n-1阶走一步和从第n-2阶走两步两种
 */
function climbStairs(n) {
  let db = [0, 1, 2];
  for (let i = 3; i <= n; i++) {
    db[i] = db[i - 1] + db[i - 2];
  }
  return db[n];
}
